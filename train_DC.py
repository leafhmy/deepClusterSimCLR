class ConfidenceDataset:
    """
    from DeepCluster with high confidence generated with SimCLR
    """
    def __init__(self, images):
        self.images = images
        self.anno = './anno/'
        if not os.path.exists(self.anno):
            os.mkdir(self.anno)
        self._save_file_path()
        self.trans = transforms.Compose([transforms.RandomResizedCrop(size=224),
                                              transforms.RandomHorizontalFlip(),
                                              transforms.ToTensor()])

    def _save_file_path(self):
        fpath = []
        labels = []

        for path, label in self.images:
            fpath.append(path)
            labels.append(label)

        with open(self.anno + 'pseudo.txt', 'w')as f:
            for fn, l in zip(fpath, labels):
                f.write('{} {}\n'.format(fn, l))

    def getDataLoader(self):
        train_loader = DatasetLoader(self.anno, 'pseudo', self.trans)
        return train_loader


class DatasetLoader:
    def __init__(self, anno_dir, phase, data_transforms):
        self.anno = anno_dir
        self.data_transforms = data_transforms
        self.data = []
        with open(self.anno+phase+'.txt', 'r') as f:
            for item in f.readlines():
                img, label = item.split(' ')
                self.data.append((img, label.strip()))

    def __len__(self):
        return len(self.data)

